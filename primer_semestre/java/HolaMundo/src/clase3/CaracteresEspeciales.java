
public class CaracteresEspeciales {
    public static void main(String[] args) {
      var nombre = "marti";
      //salto de linea 
        System.out.println("nueva linea: \n" + nombre);
        System.out.println("espacio grande (tabulador): \t" + nombre);
        System.out.println("\t\tespacio grande (tabulador): \t" + nombre);
        
        //hace un retroseso 
        System.out.println("retroseso: \b" + nombre);
        //coomillas
        System.out.println("nueva linea: \"" + nombre + "\"");
       
    }
}
