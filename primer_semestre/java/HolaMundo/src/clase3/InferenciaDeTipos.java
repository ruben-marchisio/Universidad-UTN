
public class InferenciaDeTipos {
    public static void main(String[] args) {
        /*var; es una funcionalidad introducida en Java 10. Permite declarar variables sin especificar 
        explícitamente su tipo, dejando que el compilador lo infiera automáticamente
        basándose en el valor asignado*/
        var numero = 10;
        var texto = "hola";
        System.out.println(numero);
        System.out.println(texto);
              
    }
}
