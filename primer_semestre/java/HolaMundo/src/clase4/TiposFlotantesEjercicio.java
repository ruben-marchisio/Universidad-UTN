
public class TiposFlotantesEjercicio {
    public static void main(String[] args) {
       //tipos flotantes debemos colocar la F siempre en mayuscula cuando en decimal !
             
       float numerFloat = 100.0F;
        System.out.println("numero flatante: " + numerFloat);
        System.out.println("valor minimo de float" + Float.MIN_VALUE);
        System.out.println("valor maximo de float" + Float.MAX_VALUE);
        
        //tipo double 
        double numDouble = 13556;
        System.out.println("numero : " + numerFloat);
        System.out.println("valor minimo de double" + Double.MIN_VALUE);
        System.out.println("valor maximo de double" + Double.MAX_VALUE);
        
    }
}
