// Tipos de datos en JavaScript
// En JavaScript, los tipos de datos son dinámicos y se pueden cambiar en tiempo de ejecución.
let nombre = "Ruben"; //string
let edad = 30; //number 
let esEstudiante = true; //boolean
let altura = 1.75; //number
console.log(`Hola, mi nombre es ${nombre}, tengo ${edad} años y mido ${altura} metros.`); //string

// esto es un objeto
// Un objeto es una colección de propiedades y métodos.
let Object = { 
    nombre: "Ruben",
    edad: 30,
    esEstudiante: true,
    altura: 1.75
}; 

console.log(Object);
