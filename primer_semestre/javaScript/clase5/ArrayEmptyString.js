// tipos de datos array y enmpty string
// En JavaScript, un array es un objeto que permite almacenar múltiples valores en una sola variable.
// Un array puede contener elementos de diferentes tipos de datos, incluyendo números, cadenas de texto, objetos y otros arrays.
// Los arrays son muy útiles para manejar colecciones de datos y realizar operaciones sobre ellos, como agregar, eliminar o modificar elementos.

var autos = ["BMW", "Volvo", "Mini", "ford"];
console.log(autos); // Imprime el array completo
console.log(autos[0]); // Imprime el primer elemento del array (BMW)
console.log(typeof autos); // Imprime "object", ya que los arrays son objetos en JavaScript
console.log(autos.length); // Imprime la longitud del array (4 elementos)

var vacio = []; // Un array vacío
console.log(vacio); // Imprime el array vacío
console.log(typeof vacio); // Imprime "object", ya que los arrays son objetos en JavaScript

