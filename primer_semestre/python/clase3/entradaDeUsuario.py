#entrada del usuario
# la entrada del usuario es una forma de interactuar con el programa y obtener datos de entrada
# desde el teclado o desde un archivo.
# en Python, podemos usar la funcion input() para obtener datos de entrada del usuario.
# la funcion input() detiene la ejecucion del programa y espera a que el usuario ingrese un valor.
# una vez que el usuario ingresa un valor y presiona Enter, la funcion input() devuelve ese valor como una cadena de texto.

resultado = input("ingrese un numero: ") 
print("el resultado es: ", resultado) # el resultado es una cadena de texto

# podemos  convertir el resultado a un numero entero o flotante

