#los tipos de cadena son str

#podemos unir cadenas con el operador +

frace1 = "Hola Mundo"
frace2 = "desde Python"
# atraves de una variable
frace3 = frace1 + " " + frace2
print(frace3)

# tambien podemos solo la funcion print
print(frace1 + " " + frace2)
# podemos separar las cadenas con comas o espacios
print(frace1, frace2)

# podemos concatenar numeros y cadenas
# pero debemos convertir el numero a cadena 

numero = "10"
numero2 = "20"
# podemos solo concatenar cadenas
print(numero + numero2)
# podemos convertir la cadena a entero o flotante
# y luego sumarlos
# con la funcion int()  
print(int(numero) + int(numero2))
  
