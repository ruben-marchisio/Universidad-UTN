# Tipos de datos en Python

# Enteros (int)
numero_entero = 42
print("Entero:", numero_entero, type(numero_entero))

# Flotantes (float)
numero_flotante = 3.14159
print("Flotante:", numero_flotante, type(numero_flotante))

# Cadenas de texto (str)
cadena = "Hola, mundo"
print("Cadena:", cadena, type(cadena))

# Booleanos (bool)
es_verdadero = True
print("Booleano:", es_verdadero, type(es_verdadero))

# Listas (list)
lista = [1, 2, 3, 4, 5]
print("Lista:", lista, type(lista))

# Tuplas (tuple)
tupla = (1, 2, 3, 4, 5)
print("Tupla:", tupla, type(tupla))

# Conjuntos (set)
conjunto = {1, 2, 3, 4, 5}
print("Conjunto:", conjunto, type(conjunto))

# Diccionarios (dict)
diccionario = {"clave1": "valor1", "clave2": "valor2"}
print("Diccionario:", diccionario, type(diccionario))

# NoneType
valor_nulo = None
print("NoneType:", valor_nulo, type(valor_nulo))