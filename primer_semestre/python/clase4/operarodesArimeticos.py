# Operaciones aritméticas
# Sumar dos números
operandoA = 8
operandoB = 5
suma = operandoA + operandoB
print("La suma es: ", (suma))

#esto es una interpolación de cadenas
print(f"El resultado es {suma}")

#operador resta 

resta = operandoA - operandoB
print(f"La resta es: {resta}")

#operador multiplicación
multiplicacion = operandoA * operandoB
print(f"La multiplicacion es: {multiplicacion}")

#operador division
division = operandoA / operandoB
print(f"La division es: {division}")

#pomdemos usar el operador de division entera
divisionEntera = operandoA // operandoB 
print(f"La division entera es: {divisionEntera}")

#operador modulo o residuo
modulo = operandoA % operandoB  
print(f"El modulo es: {modulo}")

#operador potencia o exponenciación
exponenciacion = operandoA ** operandoB
print(f"La exponenciacion es: {exponenciacion}")

