//  Funciones con return
// Sirven para que la función devuelva un valor y podamos usarlo en otra parte del código.


// Declaración de la función
function sumar(a, b) {

  return a + b; 
}

// Usamos la función y guardamos el resultado en una variable
let resultado = sumar(2, 3);

// Mostramos el valor devuelto en consola
console.log(resultado); 