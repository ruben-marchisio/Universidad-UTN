# 5.4 Funciones en Python
# Una función es un bloque de código que solo se ejecuta cuando es llamado.
# Las funciones pueden recibir datos, llamados parámetros, y pueden devolver datos como resultado.
# En Python, las funciones se definen usando la palabra clave `def`, seguida del nombre

def mi_funcion():
    print("Hola desde mi función!")

# llamamos a la función
mi_funcion()
